import React from 'react';

function RuleDifferences() {
  return (
    <div>
      <h2>The Following Rules Are In Effect For Oldskool Format:</h2>
      <h4>Clan Mixing</h4>
      <p>Cards from all clans can be played together without restrictions.</p>
      <h4>Mulligan</h4>
      <p>Players may "partial" mulligan their opening hand. Before the game begins, both players may shuffle any number of cards from their hand into their deck. After shuffling, each player draws until they have 5 cards in their hand.</p>
      <p>This differs from the mulligan rules in modern CFVG where players may only perform "full" mulligans.</p>
    </div>
  )
}
